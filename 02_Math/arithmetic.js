const students = 30;

//arithmetic operators

//adding +1
students = students + 1;
//subtracting -1
students = students - 1;
//multiplying by 2
students = students * 2;
//dividing by 2
students = students / 1;

//easier way to do it/ modern way
//augmented arithmetic operators
//adding +1
students += 1;
//subtracting -1
students -= 1;
//multiplying by 2
students *= 2;
//dividing by 2
students /= 2;
